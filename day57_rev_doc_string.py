def sum(x,y):
    ''' This function take two arguments and after that return sum of two numbers
    '''
    return x+y
result = sum(5,6)
print(f"SUM : {result}")
print(sum.__doc__)
# Another example
def sub(x,y):
    '''
    this function take two arguments and return substraction of that
    '''
    return x - y
res = sub(10,5)
print(f"Substraction : {res}")
print(sub.__doc__)

